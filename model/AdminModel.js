const mongooge = require("mongoose");

const adminSchema = mongooge.Schema({
  username: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
    unique: true,
  },
  password: {
    type: String,
    require: true,
  },
});

module.exports = mongooge.model("Admin",adminSchema);
